const [a, b, c, d] = [2, 4, 6, 8];

/*
  Tipos de operadores
  ++operando -> dos tipos prefix pois vem antes da chamada do operador
  operando++ -> postfix pois vem depois da chamda do operador
  operando1 + operando 2 -> infix pois vem entre dois operandos
  -operando -> operador unario pois atua somente sobre um operando  
*/

const soma = a + b + c + d; //Operador de soma "+"
const subtracao = d - a; //Operador de subtração "-"
const multiplcacao = a * c; //Operador de multiplcação "*"
const divisão = d / b //Opeador de mutiplicação "/"
const modulo = d % a //Operador de módulo
