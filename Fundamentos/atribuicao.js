const a = 7 // "=" operador de atribuição
let b = 3

console.log(b += a) //Atribuição aditiva que além da atribuição faz a soma ou seja b = b + a
console.log(b -= a) //Atribuição subtratva b = b - a
console.log(b *= a) //Atribuição mutiplicativa b = b * a
console.log(b /= a) //Atribuição divisiva b = b / a
console.log(b %= a) //Atribuição modular b = b % a